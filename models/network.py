class Network(nn.Module):
    def __init__(self, resnet, feature_dim, class_num):
        super(Network, self).__init__()
        self.resnet = resnet
        self.feature_dim = feature_dim
        self.cluster_num = class_num
        self.instance_projector = nn.Sequential(
            # nn.BatchNorm1d(512),
            nn.ReLU(),
            nn.Linear(512, 512),
            # nn.BatchNorm1d(512),
            nn.ReLU(),
            nn.Linear(512, self.feature_dim),
        )
        self.cluster_projector = nn.Sequential(
            # nn.BatchNorm1d(512),
            nn.ReLU(),
            nn.Linear(512, 512),
            # nn.BatchNorm1d(512),
            nn.ReLU(),
            nn.Linear(512, self.cluster_num),
        )
        trunc_normal_(self.cluster_projector[1].weight, std=0.02)
        trunc_normal_(self.cluster_projector[3].weight, std=0.02)

    def forward(self, x_i, x_j, return_weak=True):
        h_i = self.resnet(x_i)
        h_j = self.resnet(x_j)

        z_i = normalize(self.instance_projector(h_i), dim=1)
        z_j = normalize(self.instance_projector(h_j), dim=1)

        c_j = self.cluster_projector(h_j)

        if return_weak:
            c_i = self.cluster_projector(h_i)
            return z_i, z_j, c_i, c_j

        return z_i, z_j, c_j

    def forward_cluster(self, x):
        h = self.resnet(x)
        c = self.cluster_projector(h)
        c = torch.argmax(c, dim=1)
        return c

    def forward_cluster_confidence(self, x, soft=True):
        h = self.resnet(x)
        c = self.cluster_projector(h)
        if soft:
            c = torch.nn.functional.softmax(c, dim=1)
        return c

    def forward_embedding(self, x):
        h = self.resnet(x)
        embedding = normalize(self.instance_projector(h), dim=1, p=2)
        return embedding

    def forward_embedding_512(self, x):
        h = self.resnet(x)
        embedding_512 = normalize(h, dim=1, p=2)
        return embedding_512
